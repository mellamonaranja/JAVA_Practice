package com.mathex;

public class MathEx02 {

	public static void main(String[] args) {
		System.out.println(Math.E);

		System.out.println(Math.PI);
		
		System.out.println("square root of 2 is " + Math.sqrt(2));
		
		double radian43=Math.toRadians(43);
		System.out.println(Math.sin(radian43));
//		radian is the unit of angle, degree
//		the method to get sine value
		System.out.println(Math.cos(radian43));
		System.out.println(Math.tan(radian43));
		
		System.out.println(Math.log(27));
		System.out.println(Math.pow(2, 4));
//		the method to get multiplier
	}

}
