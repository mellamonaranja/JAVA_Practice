
public class ForEx02 {

	public static void main(String[] args) {
		
		for(int i=5 ; i >= 1 ; i--) {
			System.out.println("i: " + i);
		}
		System.out.println("프로그램 종료");
				
		
		for(int i=2; i<=10; i++) {
			if(i%2==0) {
			System.out.println("i: " + i);
		}
		}
		for(int i=2; i<=10; i+=2) {
			System.out.println("i:" + i);
		}
			
	}

}
