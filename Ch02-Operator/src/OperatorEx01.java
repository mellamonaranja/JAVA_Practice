
public class OperatorEx01 {
	public static void main(String[] args) {
		
		int n1;
		n1 = 7;
		
		int n2 = 3;
		
	int result	= n1 + n2;
	System.out.println("오 써쎀: " + result);
	
	result = n1 - n2;
	System.out.println("짋 써쎀: " + result);
	
	result = n1 * n2;
	System.out.println("썼 써쎀: " + result);
	
	result = n1 / n2;
	System.out.println("씱얋 써쎀: " + result);
	
	result = n1 % n2;
	System.out.println("씱쟵쵔 써쎀: " + result);
	
		
	}

}
